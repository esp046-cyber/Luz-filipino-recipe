// Display recipes in the grid
function displayRecipes(recipes) {
    const recipeGrid = document.getElementById('recipeGrid');
    if (!recipeGrid) {
        console.error('Recipe grid element not found!');
        return;
    }
    
    // Create recipe cards as HTML strings and join them
    const recipeHTML = recipes.map(recipe => createRecipeCard(recipe)).join('');
    
    // Set all recipes at once
    recipeGrid.innerHTML = recipeHTML;
    
    console.log(`‚úÖ Displayed ${recipes.length} recipes in the grid`);
}

// Create individual recipe card
function createRecipeCard(recipe) {
    return `<div class="recipe-card" onclick="showRecipeDetail(${JSON.stringify(recipe).replace(/"/g, '&quot;')})" data-id="${recipe.id}">
        <div class="recipe-image">${recipe.image}</div>
        <div class="recipe-content">
            <h3 class="recipe-title">${recipe.name}</h3>
            <p class="recipe-description">${recipe.description}</p>
            <div class="recipe-meta">
                <span class="prep-time">‚è±Ô∏è ${recipe.prepTime}</span>
                <span class="cook-time">üî• ${recipe.cookTime}</span>
                <span class="difficulty">‚≠ê ${recipe.difficulty}</span>
            </div>
            <div class="recipe-tags">
                ${recipe.tags.slice(0, 3).map(tag => `<span class="tag">${tag}</span>`).join('')}
            </div>
        </div>
    </div>`;
}

// Show recipe details in modal
function showRecipeDetail(recipe) {
    document.getElementById('modalTitle').textContent = recipe.name;
    document.getElementById('modalImage').textContent = recipe.image;
    document.getElementById('modalMeta').innerHTML = `
        <span>‚è±Ô∏è Prep: ${recipe.prepTime}</span>
        <span>üî• Cook: ${recipe.cookTime}</span>
        <span>‚≠ê Difficulty: ${recipe.difficulty}</span>
    `;
    document.getElementById('modalTags').innerHTML = recipe.tags.map(tag => `<span class="tag">${tag}</span>`).join('');
    document.getElementById('modalIngredients').innerHTML = recipe.ingredients.map(ing => `<li>${ing}</li>`).join('');
    document.getElementById('modalInstructions').innerHTML = recipe.instructions.map(inst => `<li>${inst}</li>`).join('');
    document.getElementById('modalNutrition').innerHTML = `
        <h3>Nutrition Facts</h3>
        <p>Calories: ${recipe.nutrition.calories} | Protein: ${recipe.nutrition.protein} | Carbs: ${recipe.nutrition.carbs} | Fat: ${recipe.nutrition.fat}</p>
    `;
    
    document.getElementById('modalOverlay').style.display = 'flex';
}

// Setup event listeners
function setupEventListeners() {
    // Modal close
    document.getElementById('modalClose').addEventListener('click', () => {
        document.getElementById('modalOverlay').style.display = 'none';
    });
    
    // Modal overlay click to close
    document.getElementById('modalOverlay').addEventListener('click', (e) => {
        if (e.target.id === 'modalOverlay') {
            document.getElementById('modalOverlay').style.display = 'none';
        }
    });
    
    // Search functionality
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            const searchTerm = e.target.value.toLowerCase();
            const filteredRecipes = filipinoRecipes.filter(recipe => 
                recipe.name.toLowerCase().includes(searchTerm) ||
                recipe.description.toLowerCase().includes(searchTerm) ||
                recipe.ingredients.some(ing => ing.toLowerCase().includes(searchTerm))
            );
            displayRecipes(filteredRecipes);
        });
    }
    
    // Filter buttons
    const filterButtons = document.querySelectorAll('.filter-pill');
    filterButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            // Remove active class from all buttons
            filterButtons.forEach(btn => btn.classList.remove('active'));
            // Add active class to clicked button
            e.target.classList.add('active');
            
            const category = e.target.dataset.category;
            const filteredRecipes = category === 'all' 
                ? filipinoRecipes 
                : filipinoRecipes.filter(recipe => recipe.category === category);
            
            displayRecipes(filteredRecipes);
        });
    });
    
    console.log('‚úÖ Event listeners setup complete');
}

